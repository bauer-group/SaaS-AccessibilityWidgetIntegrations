// ── apply / check / CLI ─────────────────────────────────────────────────────
function applyConfig() {
  validateSchema();
  const touched = [];
  for (const t of TARGETS) {
    if (t.mode === 'full') {
      write(t.file, t.content());
    } else {
      const current = read(t.file);
      write(t.file, replaceBetween(current, t.begin, t.end, t.inner()));
    }
    touched.push(t.file);
  }
  return touched;
}

function check() {
  validateSchema();
  const drift = [];
  for (const t of TARGETS) {
    if (!existsSync(join(ROOT, t.file))) {
      drift.push(`${t.file}: missing`);
      continue;
    }
    if (t.mode === 'full') {
      if (t.binary) {
        if (!readFileSync(join(ROOT, t.file)).equals(t.content())) {
          drift.push(`${t.file}: out of date`);
        }
      } else if (read(t.file) !== t.content()) {
        drift.push(`${t.file}: out of date`);
      }
    } else {
      const region = extractBetween(read(t.file), t.begin, t.end);
      if (region === null) drift.push(`${t.file}: markers not found`);
      else if (region !== t.inner()) drift.push(`${t.file}: generated region out of date`);
    }
  }
  if (drift.length) {
    console.error('Config drift — regenerate with `pnpm generate-config`:');
    for (const d of drift) console.error(`  ${d}`);
    process.exit(1);
  }
  console.log(`All ${TARGETS.length} generated targets up to date ✓`);
}

const arg = process.argv[2];
if (arg === '--check') {
  check();
} else if (!arg) {
  const touched = applyConfig();
  console.log(`Generated ${touched.length} files from config/widget-config.mjs:`);
  for (const f of touched) console.log(`  ${f}`);
} else {
  console.error('Usage: node scripts/generate-config.mjs [--check]');
  process.exit(1);
}
