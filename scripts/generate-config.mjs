/** Flatten a neutral field to English strings (WordPress translates via gettext). */
function wpField(f) {
  const out = { key: f.key, group: f.group, type: f.type, label: f.label.en };
  if (f.help?.en) out.help = f.help.en;
  if (f.default !== undefined) out.default = f.default;
  if (f.runtimeKey) out.runtimeKey = f.runtimeKey;
  if (f.asset) out.asset = f.asset;
  if (f.special) out.special = f.special;
  if (f.validate) out.validate = f.validate;
  if (f.nullable) out.nullable = true;
  if (f.emptyMeansDefault) out.emptyMeansDefault = true;
  if (f.min !== undefined) out.min = f.min;
  if (f.feature) out.feature = f.feature;
  if (f.options) {
    out.optionsTranslatable = !!f.optionsTranslatable;
    out.options = f.options.map((opt) => ({
      value: opt.value,
      label: typeof opt.label === 'string' ? opt.label : opt.label.en,
    }));
  }
  return out;
}

