#!/usr/bin/env node
/**
 * Config generator for the integrations monorepo.
 *
 * Reads the single source of truth (`config/widget-config.mjs`) and writes every
 * integration's native settings surface, i18n catalogs and the typed TS config —
 * so all 13 integrations stay in lockstep from one definition.
 *
 *   node scripts/generate-config.mjs            # write all targets
 *   node scripts/generate-config.mjs --check    # CI drift guard; exits 1 on drift
 *
 * Mirrors `scripts/sync-version.mjs`: a single TARGETS table, an idempotent
 * writer, and a --check mode that recomputes every target and diffs against disk.
 * Fully-generated files are written whole; mixed files are patched between
 * `BEGIN GENERATED … END GENERATED` markers (the rest stays hand-written).
 *
 * --check additionally fails if any option/group/feature label or help text is
 * missing its `en` or `de` string — the structural guarantee that the admin UIs
 * never drift back into a mixed-language state.
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';
import * as S from '../config/widget-config.mjs';

const ROOT = join(dirname(fileURLToPath(import.meta.url)), '..');

// ── fs helpers ──────────────────────────────────────────────────────────────
const read = (rel) => readFileSync(join(ROOT, rel), 'utf8');
function write(rel, content) {
  const abs = join(ROOT, rel);
  mkdirSync(dirname(abs), { recursive: true });
  writeFileSync(abs, content);
}

/** Replace the text between two literal markers (markers themselves preserved). */
function replaceBetween(content, begin, end, inner) {
  const b = content.indexOf(begin);
  if (b === -1) throw new Error(`Begin marker not found: ${begin}`);
  const e = content.indexOf(end, b + begin.length);
  if (e === -1) throw new Error(`End marker not found: ${end}`);
  return content.slice(0, b + begin.length) + inner + content.slice(e);
}

/** Extract the current text between two markers (for --check). */
function extractBetween(content, begin, end) {
  const b = content.indexOf(begin);
  const e = b === -1 ? -1 : content.indexOf(end, b + begin.length);
  if (b === -1 || e === -1) return null;
  return content.slice(b + begin.length, e);
}

// ── schema-derived runtime helpers (shared by emitters) ─────────────────────
/** Scalar options (everything except the synthetic `features` block). */
const SCALARS = S.OPTIONS.filter((o) => o.type !== 'features');
/** The per-feature setting keys, e.g. `feature_fontSize`. */
const featureKey = (id) => `${S.FEATURE_KEY_PREFIX}${id}`;

// ── TypeScript types emitter (shared by all 6 JS wrapper packages) ──────────
function tsTypeAlias(name, values) {
  const inline = values.map((v) => `'${v}'`).join(' | ');
  const oneLine = `export type ${name} = ${inline};`;
  if (oneLine.length <= 100) return oneLine;
  return `export type ${name} =\n${values.map((v) => `  | '${v}'`).join('\n')};`;
}

function tsConfigInterface() {
  const props = [];
  let offsetDone = false;
  for (const o of S.OPTIONS) {
    if (o.asset === 'base') continue; // admin-only; resolves into corePath/cssPath
    if (o.runtimeKey?.startsWith('offset.')) {
      if (!offsetDone) {
        props.push('  offset?: { x: number; y: number };');
        offsetDone = true;
      }
      continue;
    }
    if (o.type === 'features') {
      props.push('  initialFeatures?: Partial<Record<FeatureId, boolean>>;');
      props.push('  disabledFeatures?: FeatureId[];');
      continue;
    }
    let t;
    if (o.type === 'enum') t = o.source === 'positions' ? 'WidgetPosition' : 'WidgetLocale';
    else if (o.type === 'int') t = 'number';
    else if (o.type === 'bool') t = 'boolean';
    else t = 'string';
    if (o.special === 'keyboardShortcut') t = 'string | false';
    const tsNull =
      o.asset === 'coreIntegrity' || o.asset === 'cssIntegrity' || (o.nullable && !o.asset);
    if (tsNull) t = `${t} | null`;
    props.push(`  ${o.key}?: ${t};`);
  }
  props.push('  [key: string]: unknown;');
  return props.join('\n');
}

function emitTsTypes() {
  return `// AUTO-GENERATED by scripts/generate-config.mjs from config/widget-config.mjs.
// Do not edit by hand — run \`pnpm generate-config\` to regenerate.

${tsTypeAlias('WidgetPosition', S.POSITIONS)}

${tsTypeAlias('WidgetLocale', S.LOCALES)}

${tsTypeAlias(
  'FeatureId',
  S.FEATURES.map((f) => f.id),
)}

/**
 * Full runtime configuration accepted by \`window.AccessibilityWidgetConfig\`.
 * Generated from the shared schema; unknown keys pass through unchanged so the
 * wrapper stays forward-compatible with newer widget releases.
 */
export interface WidgetConfig {
${tsConfigInterface()}
}
`;
}

// ── shared i18n (stable key + en + de for every translatable schema string) ─
/**
 * The canonical translatable set. `key` is a stable identifier for keyed-i18n
 * platforms (TYPO3 `LLL:`, Shopify `t:`); `en`/`de` drive gettext-style catalogs
 * (WordPress, Drupal, Magento) which key by the English source string.
 */
function i18nEntries() {
  const out = [];
  for (const g of S.GROUPS) out.push({ key: `group.${g.id}`, en: g.label.en, de: g.label.de });
  for (const o of SCALARS) {
    out.push({ key: `option.${o.key}.label`, en: o.i18n.label.en, de: o.i18n.label.de });
    out.push({ key: `option.${o.key}.help`, en: o.i18n.help.en, de: o.i18n.help.de });
  }
  const featuresBlock = S.OPTIONS.find((o) => o.type === 'features');
  out.push({
    key: 'features.help',
    en: featuresBlock.i18n.help.en,
    de: featuresBlock.i18n.help.de,
  });
  for (const f of S.FEATURES) out.push({ key: `feature.${f.id}`, en: f.label.en, de: f.label.de });
  for (const st of S.FEATURE_STATES) {
    out.push({ key: `featureState.${st.value}`, en: st.label.en, de: st.label.de });
  }
  return out;
}

/** Dedupe a list of {en,de,...} by English source (gettext catalogs key by msgid). */
function dedupeByEn(pairs) {
  const seen = new Set();
  return pairs.filter((p) => (seen.has(p.en) ? false : seen.add(p.en)));
}

// ── PHP value serializer (for generated *.php descriptor files) ─────────────
const phpStr = (s) => `'${String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'")}'`;
function phpValue(v, indent = '') {
  if (v === true) return 'true';
  if (v === false) return 'false';
  if (v === null) return 'null';
  if (typeof v === 'number') return String(v);
  if (typeof v === 'string') return phpStr(v);
  const inner = indent + '  ';
  if (Array.isArray(v)) {
    if (v.length === 0) return '[]';
    return `[\n${v.map((x) => inner + phpValue(x, inner)).join(',\n')},\n${indent}]`;
  }
  const entries = Object.entries(v);
  if (entries.length === 0) return '[]';
  return `[\n${entries
    .map(([k, val]) => `${inner}${phpStr(k)} => ${phpValue(val, inner)}`)
    .join(',\n')},\n${indent}]`;
}

// ── flat-field model (one admin setting per scalar option + one per feature) ─
/**
 * Expand the schema into the flat list of admin settings every server platform
 * shares (one tri-state field per feature). Labels/help are carried as `{en,de}`;
 * enum option labels are plain strings (positions raw, locales endonyms) and never
 * translated, while feature-state option labels are `{en,de}`. Per-platform
 * emitters flatten this to whatever their i18n mechanism needs.
 */
function flatFields() {
  const fields = [];
  for (const o of S.OPTIONS) {
    if (o.type === 'features') {
      for (const f of S.FEATURES) {
        fields.push({
          key: featureKey(f.id),
          group: 'features',
          type: 'feature_state',
          default: S.FEATURE_STATE_DEFAULT,
          feature: f.id,
          label: { en: f.label.en, de: f.label.de },
          optionsTranslatable: true,
          options: S.FEATURE_STATES.map((st) => ({
            value: st.value,
            label: { en: st.label.en, de: st.label.de },
          })),
        });
      }
      continue;
    }
    const field = {
      key: o.key,
      group: o.group,
      type: o.type,
      label: { en: o.i18n.label.en, de: o.i18n.label.de },
      help: { en: o.i18n.help.en, de: o.i18n.help.de },
    };
    if (o.default !== undefined) field.default = o.default;
    if (o.runtimeKey) field.runtimeKey = o.runtimeKey;
    if (o.asset) field.asset = o.asset;
    if (o.special) field.special = o.special;
    if (o.validate) field.validate = o.validate;
    if (o.nullable) field.nullable = true;
    if (o.emptyMeansDefault) field.emptyMeansDefault = true;
    if (o.min !== undefined) field.min = o.min;
    if (o.type === 'enum') {
      field.optionsTranslatable = false;
      field.options = S.enumValues(o.source).map((v) => ({
        value: v,
        label: S.enumLabel(o.source, v),
      }));
    }
    fields.push(field);
  }
  return fields;
}

/** Drupal config schema type for a field. */
const drupalType = (f) => (f.type === 'bool' ? 'boolean' : f.type === 'int' ? 'integer' : 'string');

/** YAML scalar (booleans/ints bare, strings single-quoted). */
function yamlScalar(v) {
  if (typeof v === 'boolean') return v ? 'true' : 'false';
  if (typeof v === 'number') return String(v);
  return `'${String(v).replace(/'/g, "''")}'`;
}

/** Default value a field stores at rest (used by install YAML + descriptors). */
function fieldDefault(f) {
  if (f.default !== undefined) return f.default;
  return f.type === 'bool' ? false : f.type === 'int' ? 0 : '';
}

