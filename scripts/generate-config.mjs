function emitShopifyLocale(lang) {
  const settings = { group: {} };
  for (const g of S.GROUPS) settings.group[g.id] = g.label[lang];
  for (const o of SCALARS) {
    settings[o.key] = { label: o.i18n.label[lang] };
    if (o.i18n.help.en) settings[o.key].info = o.i18n.help[lang];
  }
  const ids = S.FEATURES.map((f) => f.id).join(', ');
  const prefixEnabled =
    lang === 'de'
      ? 'Kommagetrennte Funktions-IDs, standardmäßig aktiviert: '
      : 'Comma-separated feature IDs to enable by default: ';
  const prefixDisabled =
    lang === 'de'
      ? 'Kommagetrennte Funktions-IDs, die ausgeblendet werden: '
      : 'Comma-separated feature IDs to hide: ';
  settings.initiallyEnabled = {
    label: lang === 'de' ? 'Standardmäßig aktivierte Funktionen' : 'Initially enabled features',
    info: prefixEnabled + ids,
  };
  settings.disabledFeatures = {
    label: lang === 'de' ? 'Ausgeblendete Funktionen' : 'Hidden features',
    info: prefixDisabled + ids,
  };
  return `${JSON.stringify({ settings }, null, 2)}\n`;
}

// ── TYPO3 emitters ──────────────────────────────────────────────────────────
// Settings live in Extension Configuration (ext_conf_template.txt, auto-rendered
// GUI). Like Shopify, the 15 features collapse to two comma-separated string
// fields. A PSR-15 middleware injects the config securely (replaces the previous
// raw TypoScript interpolation). Field labels are bilingual via LLL/XLIFF.
const TYPO3_DIR = 'packages/cms/typo3/accessibility_widget';
const TYPO3_LLL = 'LLL:EXT:accessibility_widget/Resources/Private/Language/locallang.xlf:';

/** Structural descriptor (compact features) consumed by the middleware. */
function typo3Fields() {
  const fields = SCALARS.map((o) => {
    const out = { key: o.key, type: o.type };
    if (o.default !== undefined) out.default = o.default;
    if (o.runtimeKey) out.runtimeKey = o.runtimeKey;
    if (o.asset) out.asset = o.asset;
    if (o.special) out.special = o.special;
    if (o.min !== undefined) out.min = o.min;
    if (o.validate) out.validate = o.validate;
    return out;
  });
  fields.push({ key: 'initiallyEnabled', type: 'features-list', mode: 'enable' });
  fields.push({ key: 'disabledFeatures', type: 'features-list', mode: 'disable' });
  return fields;
}

const typo3ConfType = (o) => {
  if (o.type === 'enum') {
    const opts = S.enumValues(o.source)
      .map((v) => `${S.enumLabel(o.source, v)}=${v}`)
      .join(',');
    return `options[${opts}]`;
  }
  if (o.type === 'bool') return 'boolean';
  if (o.type === 'int') return 'int+';
  return 'string';
};

const typo3ConfDefault = (o) => {
  if (o.type === 'bool') return fieldDefault(o) ? '1' : '0';
  if (o.type === 'int') return String(fieldDefault(o));
  return o.default !== undefined ? String(o.default) : '';
};

function emitTypo3ConfTemplate() {
  const lines = [];
  for (const g of S.GROUPS) {
    let order = 0;
    if (g.id === 'features') {
      for (const key of ['initiallyEnabled', 'disabledFeatures']) {
        order += 10;
        lines.push(`# cat=${g.id}/a/${order}; type=string; label=${TYPO3_LLL}option.${key}.label`);
        lines.push(`${key} =`);
        lines.push('');
      }
    } else {
      for (const o of SCALARS.filter((s) => s.group === g.id)) {
        order += 10;
        lines.push(
          `# cat=${g.id}/a/${order}; type=${typo3ConfType(o)}; label=${TYPO3_LLL}option.${o.key}.label`,
        );
        lines.push(`${o.key} = ${typo3ConfDefault(o)}`);
        lines.push('');
      }
    }
  }
  return `${lines.join('\n').trimEnd()}\n`;
}

