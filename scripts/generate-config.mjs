function shopwareDefault(f) {
  if (f.type === 'bool') return fieldDefault(f) ? 'true' : 'false';
  if (f.type === 'int') return String(fieldDefault(f));
  return f.default !== undefined && f.default !== '' ? String(f.default) : null;
}

function shopwareOptionsXml(f) {
  return f.options
    .map((opt) => {
      if (typeof opt.label === 'string') {
        return `        <option><id>${xmlEscape(opt.value)}</id><name>${xmlEscape(opt.label)}</name></option>`;
      }
      return `        <option>
          <id>${xmlEscape(opt.value)}</id>
          <name>${xmlEscape(opt.label.en)}</name>
          <name lang="de-DE">${xmlEscape(opt.label.de)}</name>
        </option>`;
    })
    .join('\n');
}

function emitShopwareConfigXml() {
  const cards = S.GROUPS.map((g) => {
    const fieldsXml = flatFields()
      .filter((f) => f.group === g.id)
      .map((f) => {
        const def = shopwareDefault(f);
        const lines = [
          `    <input-field type="${shopwareFieldType(f)}">`,
          `      <name>${f.key}</name>`,
        ];
        if (def !== null) lines.push(`      <defaultValue>${xmlEscape(def)}</defaultValue>`);
        lines.push(`      <label>${xmlEscape(f.label.en)}</label>`);
        lines.push(`      <label lang="de-DE">${xmlEscape(f.label.de)}</label>`);
        if (f.help?.en) {
          lines.push(`      <helpText>${xmlEscape(f.help.en)}</helpText>`);
          lines.push(`      <helpText lang="de-DE">${xmlEscape(f.help.de)}</helpText>`);
        }
        if (f.options) {
          lines.push('      <options>');
          lines.push(shopwareOptionsXml(f));
          lines.push('      </options>');
        }
        lines.push('    </input-field>');
        return lines.join('\n');
      })
      .join('\n\n');
    return `  <card>
    <title>${xmlEscape(g.label.en)}</title>
    <title lang="de-DE">${xmlEscape(g.label.de)}</title>

${fieldsXml}
  </card>`;
  }).join('\n\n');

  return `<?xml version="1.0" encoding="UTF-8"?>
<config xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance"
        xsi:noNamespaceSchemaLocation="https://raw.githubusercontent.com/shopware/platform/trunk/src/Core/System/SystemConfig/Schema/config.xsd">
${cards}
</config>
`;
}

