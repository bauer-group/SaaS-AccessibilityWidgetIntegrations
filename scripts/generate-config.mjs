// ── Magento emitters ────────────────────────────────────────────────────────
const MAGENTO_DIR = 'packages/shop/magento/BauerGroup_AccessibilityWidget';
const MAGENTO_SRC_NS = 'BauerGroup\\AccessibilityWidget\\Model\\Config\\Source\\';
const xmlEscape = (s) =>
  String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;');

/** A select for enum/feature/bool, a text input otherwise. */
const magentoFieldType = (f) =>
  f.type === 'enum' || f.type === 'feature_state' || f.type === 'bool' ? 'select' : 'text';

function magentoSourceModel(f) {
  if (f.type === 'bool') return 'Magento\\Config\\Model\\Config\\Source\\Yesno';
  if (f.type === 'feature_state') return `${MAGENTO_SRC_NS}FeatureState`;
  if (f.key === 'position') return `${MAGENTO_SRC_NS}Position`;
  if (f.key === 'locale') return `${MAGENTO_SRC_NS}Locale`;
  return null;
}

/** Default for etc/config.xml (booleans as 1/0), or null to omit. */
function magentoDefaultValue(f) {
  if (f.type === 'bool') return fieldDefault(f) ? '1' : '0';
  if (f.type === 'int') return String(fieldDefault(f));
  return f.default !== undefined && f.default !== '' ? String(f.default) : null;
}

/**
 * Lean structural descriptor (no labels) consumed by the PHP injectors. `pathFn`
 * maps a field to its native config path.
 */
function descriptorFields(pathFn) {
  return flatFields().map((f) => {
    const out = { key: f.key, path: pathFn(f), type: f.type };
    if (f.default !== undefined) out.default = f.default;
    if (f.runtimeKey) out.runtimeKey = f.runtimeKey;
    if (f.asset) out.asset = f.asset;
    if (f.special) out.special = f.special;
    if (f.feature) out.feature = f.feature;
    if (f.min !== undefined) out.min = f.min;
    if (f.validate) out.validate = f.validate;
    return out;
  });
}

