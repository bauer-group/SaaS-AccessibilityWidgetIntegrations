const shopifySettingType = (o) =>
  o.type === 'enum'
    ? 'select'
    : o.type === 'int'
      ? 'number'
      : o.type === 'color'
        ? 'color'
        : o.type === 'bool'
          ? 'checkbox'
          : 'text';

function shopifyScalarSetting(o) {
  const s = { type: shopifySettingType(o), id: o.key, label: `t:settings.${o.key}.label` };
  if (o.i18n.help.en) s.info = `t:settings.${o.key}.info`;
  if (o.default !== undefined) s.default = o.default;
  if (o.type === 'int' && o.min !== undefined) s.min = o.min;
  if (o.type === 'enum') {
    s.options = S.enumValues(o.source).map((v) => ({ value: v, label: S.enumLabel(o.source, v) }));
  }
  return s;
}

/** The Shopify schema settings array (headers + scalars + 2 feature fields). */
function shopifySettings() {
  const out = [];
  for (const g of S.GROUPS) {
    out.push({ type: 'header', content: `t:settings.group.${g.id}` });
    if (g.id === 'features') {
      for (const id of ['initiallyEnabled', 'disabledFeatures']) {
        out.push({
          type: 'text',
          id,
          label: `t:settings.${id}.label`,
          info: `t:settings.${id}.info`,
        });
      }
    } else {
      for (const o of SCALARS.filter((s) => s.group === g.id)) out.push(shopifyScalarSetting(o));
    }
  }
  return out;
}

/** The render-tag argument list forwarding every block setting to the snippet. */
function shopifyRenderArgs() {
  const ids = [];
  for (const g of S.GROUPS) {
    if (g.id === 'features') ids.push('initiallyEnabled', 'disabledFeatures');
    else for (const o of SCALARS.filter((s) => s.group === g.id)) ids.push(o.key);
  }
  return ids.map((id) => `  ${id}: block.settings.${id}`).join(',\n');
}

