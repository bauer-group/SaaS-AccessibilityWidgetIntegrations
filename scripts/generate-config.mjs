// gettext .po/.pot/.mo --------------------------------------------------------
const poEscape = (s) => s.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');

function poFile(pairs, lang, translated) {
  const headerFields = [
    'Project-Id-Version: accessibility-widget\\n',
    'MIME-Version: 1.0\\n',
    'Content-Type: text/plain; charset=UTF-8\\n',
    'Content-Transfer-Encoding: 8bit\\n',
    `Language: ${lang}\\n`,
  ];
  const header = `msgid ""\nmsgstr ""\n${headerFields.map((h) => `"${h}"`).join('\n')}\n`;
  const body = pairs
    .map((p) => `msgid "${poEscape(p.en)}"\nmsgstr "${translated ? poEscape(p.de) : ''}"`)
    .join('\n\n');
  return `${header}\n${body}\n`;
}

function wpPoPairs() {
  return dedupeByEn([...i18nEntries(), ...WP_CHROME]);
}

const emitWpPot = () => poFile(wpPoPairs(), '', false);
const emitWpPoDe = () => poFile(wpPoPairs(), 'de_DE', true);

/** Encode a deterministic gettext .mo (sorted by msgid) — what WordPress loads. */
function emitWpMoDe() {
  const headerStr =
    'MIME-Version: 1.0\nContent-Type: text/plain; charset=UTF-8\n' +
    'Content-Transfer-Encoding: 8bit\nLanguage: de_DE\n';
  const entries = [{ id: '', str: headerStr }];
  for (const p of wpPoPairs()) entries.push({ id: p.en, str: p.de });
  entries.sort((a, b) => (a.id < b.id ? -1 : a.id > b.id ? 1 : 0));

  const n = entries.length;
  const ids = entries.map((e) => Buffer.from(e.id, 'utf8'));
  const strs = entries.map((e) => Buffer.from(e.str, 'utf8'));
  const oTable = 28;
  const tTable = oTable + n * 8;
  let dataPos = tTable + n * 8;
  const idMeta = [];
  for (const b of ids) {
    idMeta.push([b.length, dataPos]);
    dataPos += b.length + 1;
  }
  const strMeta = [];
  for (const b of strs) {
    strMeta.push([b.length, dataPos]);
    dataPos += b.length + 1;
  }
  const buf = Buffer.alloc(dataPos);
  buf.writeUInt32LE(0x950412de, 0);
  buf.writeUInt32LE(0, 4);
  buf.writeUInt32LE(n, 8);
  buf.writeUInt32LE(oTable, 12);
  buf.writeUInt32LE(tTable, 16);
  buf.writeUInt32LE(0, 20); // hash table size
  buf.writeUInt32LE(dataPos, 24); // hash table offset (unused, size 0)
  for (let i = 0; i < n; i++) {
    buf.writeUInt32LE(idMeta[i][0], oTable + i * 8);
    buf.writeUInt32LE(idMeta[i][1], oTable + i * 8 + 4);
    buf.writeUInt32LE(strMeta[i][0], tTable + i * 8);
    buf.writeUInt32LE(strMeta[i][1], tTable + i * 8 + 4);
  }
  for (let i = 0; i < n; i++) {
    ids[i].copy(buf, idMeta[i][1]);
    strs[i].copy(buf, strMeta[i][1]);
  }
  return buf;
}

