function typo3XlfUnits() {
  const units = SCALARS.map((o) => ({
    id: `option.${o.key}.label`,
    en: o.i18n.label.en,
    de: o.i18n.label.de,
  }));
  units.push({
    id: 'option.initiallyEnabled.label',
    en: 'Initially enabled features (comma-separated IDs)',
    de: 'Standardmäßig aktivierte Funktionen (kommagetrennte IDs)',
  });
  units.push({
    id: 'option.disabledFeatures.label',
    en: 'Hidden features (comma-separated IDs)',
    de: 'Ausgeblendete Funktionen (kommagetrennte IDs)',
  });
  return units;
}

function emitTypo3Xlf(lang) {
  const isDe = lang === 'de';
  const body = typo3XlfUnits()
    .map((u) => {
      const target = isDe ? `\n        <target>${xmlEscape(u.de)}</target>` : '';
      return `      <trans-unit id="${xmlEscape(u.id)}" resname="${xmlEscape(u.id)}">
        <source>${xmlEscape(u.en)}</source>${target}
      </trans-unit>`;
    })
    .join('\n');
  const langAttr = isDe ? ' target-language="de"' : '';
  return `<?xml version="1.0" encoding="UTF-8"?>
<xliff version="1.2" xmlns="urn:oasis:names:tc:xliff:document:1.2">
  <file source-language="en"${langAttr} datatype="plaintext" original="EXT:accessibility_widget/Resources/Private/Language/locallang.xlf" product-name="accessibility_widget">
    <body>
${body}
    </body>
  </file>
</xliff>
`;
}

