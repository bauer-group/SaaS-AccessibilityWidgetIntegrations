// ── schema validation (always runs; --check fails on any gap) ───────────────
function validateSchema() {
  const errors = [];
  const bothLangs = (obj, where) => {
    if (!obj || !obj.en || !obj.de) errors.push(`${where}: missing en/de`);
  };
  for (const g of S.GROUPS) bothLangs(g.label, `group ${g.id}`);
  for (const f of S.FEATURES) bothLangs(f.label, `feature ${f.id}`);
  for (const st of S.FEATURE_STATES) bothLangs(st.label, `feature-state ${st.value}`);
  for (const o of S.OPTIONS) {
    bothLangs(o.i18n?.label, `option ${o.key} label`);
    bothLangs(o.i18n?.help, `option ${o.key} help`);
    if (!S.GROUPS.some((g) => g.id === o.group)) errors.push(`option ${o.key}: unknown group`);
  }
  if (errors.length) {
    throw new Error(`Schema validation failed:\n  ${errors.join('\n  ')}`);
  }
}

